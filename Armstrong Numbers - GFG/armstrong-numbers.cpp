class Solution {
  public:
    string armstrongNumber(int n){
        // code here
        int x  = n;
        while(x!=0){
            int r = x%10;
            x = x/10;
            n -= (r*r)*r;
        }
        if(n==0){
            return "Yes";
        }
        return "No";
    }
};

